function generateHTMLContent(doc: any) {
    if (!doc || !doc.content) return '';

    return doc.content.map((node: any) => {
        switch (node.type) {
            case 'heading':
                return `<h${node.attrs.level}>${getText(node)}</h${node.attrs.level}>`;
            case 'paragraph':
                const text = getText(node);
                // Check for warning styling from our custom class
                if (node.attrs && node.attrs.class && node.attrs.class.includes('text-red-600')) {
                    return `<div class="warning"><strong>WARNING:</strong> ${text.replace('WARNING:', '')}</div>`;
                }
                return `<p>${text}</p>`;
            case 'bulletList':
                return `<ul>${node.content.map((item: any) => `<li>${generateHTMLContent(item)}</li>`).join('')}</ul>`;
            case 'orderedList':
                return `<ol>${node.content.map((item: any) => `<li>${generateHTMLContent(item)}</li>`).join('')}</ol>`;
            case 'listItem':
                return generateHTMLContent(node); // List items contain paragraphs usually
            case 'table':
                return `<table>
          ${node.content.map((row: any) => `
            <tr>${row.content.map((cell: any) => `
              <${cell.type === 'tableHeader' ? 'th' : 'td'}>${generateHTMLContent(cell)}</${cell.type === 'tableHeader' ? 'th' : 'td'}>
            `).join('')}</tr>
          `).join('')}
        </table>`;
            default:
                return '';
        }
    }).join('');
}

function getText(node: any): string {
    if (node.text) return node.text;
    if (node.content) return node.content.map(getText).join('');
    return '';
}
